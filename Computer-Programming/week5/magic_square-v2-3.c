#include <stdio.h>

int main(){
    int size;
    printf("Enter size of rectangle : ");
    scanf("%d",&size);
    if(size <= 0){
        printf("This is impossiple!");
    }else{
        for(int i = 0; i < size; i++){
            for(int j = 0; j < size; j++){
                if(i == 0 || i == size-1){
                    printf("*");
                }else{
                    if(j == 0 || j == size-1){
                        printf("*");
                    }else{
                        printf(" ");
                    }
                }
            }
            printf("\n");
        }
    }
    return 0;
}

// โปรแกรมนี้ใช้ในการสร้างรูปสี่เหลี่ยมผืนผ้า (rectangle) ที่มีขนาดข้อมูลขนาดความยาวและความกว้างที่ผู้ใช้ป้อน โดยมีการอธิบายการทำงานดังนี้:

// โปรแกรมจะแสดงข้อความ "Enter size of rectangle : " เพื่อรับขนาดของสี่เหลี่ยมผืนผ้าจากผู้ใช้ ผู้ใช้จะต้องป้อนจำนวนเต็มบวกเท่านั้น (ขนาดไม่เป็นบวกจะถือว่า "This is impossible!").

// โปรแกรมจะอ่านขนาดที่ผู้ใช้ป้อนโดยใช้ scanf และจัดเก็บขนาดในตัวแปร size.

// โปรแกรมจะตรวจสอบว่า size มีค่าเป็นบวกหรือไม่ ถ้าไม่ใช่ โปรแกรมจะแสดงข้อความ "This is impossible!" และจบการทำงาน.

// ถ้า size เป็นบวก โปรแกรมจะเริ่มลูป for ซึ่งใช้ในการสร้างและแสดงรูปสี่เหลี่ยมผืนผ้า โดยลูปนี้จะวนตามความสูงของสี่เหลี่ยมผืนผ้า (จำนวนแถว).

// ในแต่ละแถว โปรแกรมจะใช้ลูป for อีกครั้งเพื่อสร้างและแสดงสัญลักษณ์ในแต่ละคอลัมน์ของแถว โดยตรวจสอบตำแหน่งที่ต้องการแสดง "*" (ดาว) เพื่อสร้างรูปสี่เหลี่ยมผืนผ้าที่มีเส้นขอบที่ปิดอยู่ และส่วนอื่นใส่ช่องว่าง.

// โปรแกรมจะวนลูปเพื่อสร้างแถวถัดไปของสี่เหลี่ยมผืนผ้าจนครบตามขนาดที่ผู้ใช้ระบุ.

// เมื่อสร้างและแสดงสี่เหลี่ยมผืนผ้าเสร็จสิ้น โปรแกรมจะสิ้นสุดการทำงานและจบโปรแกรม.