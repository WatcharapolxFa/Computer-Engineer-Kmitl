#include<stdio.h>
int main(){
    int a[10],i,j,temp;
    printf(" *** Ascending sort ***\nEnter 10 whole numbers : ");
    for(i=0;i<10;i++){
        scanf("%d",&a[i]);
    }
    for(i=0;i<10;i++){
        for(j=i+1;j<10;j++){
            if(a[i]>a[j]){
                temp=a[i];
                a[i]=a[j];
                a[j]=temp;
            }
        }   
    }
    printf("Output : ");
    for(i=0;i<10;i++){
        printf("%d ",a[i]);
    }
    return 0;
}

// โปรแกรมนี้มีวัตถุประสงค์ในการเรียงลำดับตัวเลขจำนวนเต็ม 10 ตัวจากน้อยไปหามาก (เรียงเป็นลำดับขึ้น) โดยการทำงานของโปรแกรมมีดังนี้:

// โปรแกรมจะประกาศตัวแปร a เป็นอาร์เรย์ขนาด 10 สำหรับเก็บตัวเลขจำนวนเต็ม และตัวแปร i, j, และ temp ซึ่งใช้ในการวนลูปและสลับค่า.

// โปรแกรมจะแสดงข้อความ " *** Ascending sort ***\nEnter 10 whole numbers : " เพื่อขอให้ผู้ใช้ป้อน 10 ตัวเลขจำนวนเต็ม โดยใช้ลูป for เพื่อรับค่าที่ผู้ใช้ป้อนแล้วเก็บไว้ในอาร์เรย์ a.

// หลังจากนั้น, โปรแกรมจะใช้ลูป for ซ้อนกันสองชั้นเพื่อเรียงลำดับตัวเลขในอาร์เรย์ a โดยใช้เทคนิค Bubble Sort (การเปรียบเทียบและสลับค่า).

// ในแต่ละรอบของลูป for ซ้อนกัน, โปรแกรมจะเปรียบเทียบค่าระหว่าง a[i] และ a[j] ถ้า a[i] มากกว่า a[j] จะทำการสลับค่าด้วยตัวแปรชั่วคราว temp.

// หลังจากที่ทำการวนลูปทั้งหมด, โปรแกรมจะมีลำดับตัวเลขที่เรียงจากน้อยไปหามากในอาร์เรย์ a.

// โปรแกรมจะแสดงผลลัพธ์ที่ได้หลังจากการเรียงลำดับ ในรูปแบบ "Output : " และแสดงค่าทั้ง 10 ตัวเลขที่เรียงลำดับ.

// โปรแกรมจะสิ้นสุดการทำงาน.

// ดังนั้น, โปรแกรมนี้ทำงานโดยรับค่า 10 ตัวเลขจำนวนเต็ม และเรียงลำดับตัวเลขเหล่านี้ในลำดับเรียงขึ้นและแสดงผลลัพธ์ในภาษา C.