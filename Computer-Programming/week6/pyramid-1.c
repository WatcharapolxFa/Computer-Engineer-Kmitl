#include <stdio.h>
int main()
{
    int n;
    printf(" *** Show isosceles triangle ***\nEnter a counting number : ");
    scanf("%d", &n);
    if(n>2){
        printf("Output : \n");
        for (int i = 1; i <= n; i++){
            for (int j = n - i; j >= 1; j--)
                printf(" ");
            for (int k = 1; k <= 2 * i - 1; k++)
                printf("*");
        printf("\n");
        }
    }
    else
        printf(" --- Incorrect number. ---");
    
}

// โปรแกรมนี้ทำหน้าที่ให้ผู้ใช้ป้อนจำนวนเต็มบวก (n) และจากนั้นแสดงรูปสามเหลี่ยมลิงค์ด้านบนที่มีด้านที่มีความยาวเท่ากันและด้านที่มีความยาวเพิ่มขึ้นเรื่อย ๆ ดังนี้:

// โปรแกรมจะแสดงข้อความ " *** Show isosceles triangle ***\nEnter a counting number : " เพื่อขอให้ผู้ใช้ป้อนจำนวนเต็มบวก (n) โดยใช้ scanf เพื่อรับข้อมูลที่ผู้ใช้ป้อน.

// โปรแกรมจะตรวจสอบว่า n มีค่ามากกว่า 2 หรือไม่ (n > 2) ถ้าเป็นจริงจะดำเนินการสร้างรูปสามเหลี่ยมและแสดงผลลัพธ์ ถ้าไม่เป็นจริง โปรแกรมจะแสดง " --- Incorrect number. ---" เพื่อแจ้งให้ผู้ใช้รู้ว่าค่า n ที่ป้อนไม่ถูกต้อง.

// ในกรณีที่ n มีค่าถูกต้อง (n > 2) โปรแกรมจะใช้ลูป for ซ้อนกันสองชั้น เพื่อสร้างรูปสามเหลี่ยม โดยใช้ตัวแปร i เพื่อแทนบรรทัดปัจจุบันของรูปสามเหลี่ยม.

// ลูปชั้นภายนอก (ลูป for ที่ใช้ตัวแปร i) รันจาก i เริ่มต้นที่ 1 และเพิ่มขึ้นทีละ 1 จนถึง n.

// ภายในลูปชั้นนอก โปรแกรมจะใช้ลูป for ชั้นใน (ลูป for ที่ใช้ตัวแปร j) เพื่อพิมพ์ช่องว่างที่ต้องการหน้าสามเหลี่ยม เช่น ถ้า n = 5 และ i = 3 ในบรรทัดที่ 3 ของรูปสามเหลี่ยม โปรแกรมจะพิมพ์ 2 ช่องว่าง (n - i = 5 - 3 = 2) แล้วก็รูปดาวที่มี 5 ดวง.

// ในลูปชั้นภายใน (ลูป for ที่ใช้ตัวแปร k) โปรแกรมจะพิมพ์ดาว (*) ตามจำนวนที่เพิ่มขึ้นเรื่อย ๆ จาก 1 ถึง (2 * i - 1) เพื่อสร้างรูปสามเหลี่ยม.

// หลังจากลูป for ชั้นในทำงานเสร็จสิ้น โปรแกรมจะพิมพ์ขึ้นบรรทัดใหม่ (ใช้ printf("\n");) เพื่อขึ้นบรรทัดใหม่สำหรับรูปสามเหลี่ยมถัดไป.

// โปรแกรมจะสิ้นสุดการทำงานหลังจากแสดงรูปสามเหลี่ยมเสร็จสิ้น และผู้ใช้จะเห็นรูปสามเหลี่ยมที่ถูกสร้างขึ้นบนหน้าจอ.





