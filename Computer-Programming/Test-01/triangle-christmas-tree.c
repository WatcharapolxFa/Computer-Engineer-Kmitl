#include <stdio.h>

void draw_christmas_tree(int n) {
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n - i - 1; j++)
            printf(" ");
        for(int j = 0; j < 2 * i + 1; j++)
            printf("*");
        printf("\n");
    }
    // for(int i = 0; i < n; i++) {
    //     for(int j = 0; j < n - i - 1; j++)
    //         printf(" ");
    //     for(int j = 0; j < 2 * i + 1; j++)
    //         printf("*");
    //     printf("\n");
    // }
    for(int i = 0; i < 3; i++) {
        for(int j = 0; j < n - 2; j++)
            printf(" ");
        printf("***\n");
    }
}

int main() {
    int size;
    printf("Enter the height of the Christmas tree (excluding the base): ");
    scanf("%d", &size);
    draw_christmas_tree(size);
    return 0;
}

