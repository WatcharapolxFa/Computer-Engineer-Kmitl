#include <stdio.h>

void draw_triangle(int n) {
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n - i - 1; j++)
            printf(" ");
        for(int j = 0; j < 2 * i + 1; j++)
            printf("*");
        for(int j = 0; j < n - i - 1; j++)
            printf(" ");
        printf("\n");
    }
    for(int i = n-2; i >= 0; i--) {
        for(int j = 0; j < n - i - 1; j++)
            printf(" ");
        for(int j = 0; j < 2 * i + 1; j++)
            printf("*");
        for(int j = 0; j < n - i - 1; j++)
            printf(" ");
        printf("\n");
    }
}

int main() {
    int size;
    printf("Enter the height of the triangle: ");
    scanf("%d", &size);
    draw_triangle(size);
    return 0;
}



