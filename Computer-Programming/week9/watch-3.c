#include <stdio.h>

void sort(int ratings[]) // น้อยไปมาก
{
    int temp;
    for (int i = 0; i < 5; i++)
        for (int j = i + 1; j < 5; j++)
            if (ratings[i] > ratings[j])
            {
                temp = ratings[i];
                ratings[i] = ratings[j];
                ratings[j] = temp;
            }
}

