#include <stdio.h>
void cutify(char *p);

int main()
{
    char name[20];
    printf("Enter name : ");
    scanf("%[^\n]", &name);
    cutify(name);
    printf("Cutify name : %s", name);
}

void cutify(char *p)
{
    while (*p != '\0')
    {
        /* CODE HERE */
        if (*p == 'a' || *p == 'e' || *p == 'o' || *p == 'u')
        {
            *p = 'i';
        }
        else if (*p == 'A' || *p == 'E' || *p == 'O' || *p == 'U')
        {
            *p = 'I';
        }
        p++;
    }
}



// โปรแกรมนี้มีวัตถุประสงค์ในการรับข้อมูลชื่อ (name) จากผู้ใช้ แล้วนำชื่อนั้นมาแปลงแปลงตัวอักษร 'a', 'e', 'o', 'u' ให้เป็น 'i' และตัวอักษร 'A', 'E', 'O', 'U' ให้เป็น 'I' ซึ่งเรียกว่าการ "cutify" ชื่อ การทำงานของโปรแกรมมีดังนี้:

// โปรแกรมจะประกาศอาร์เรย์ name ขนาด 20 ตัวอักษร เพื่อเก็บชื่อที่ผู้ใช้ป้อน.

// โปรแกรมจะแสดงข้อความ "Enter name : " เพื่อขอให้ผู้ใช้ป้อนชื่อ โดยใช้ scanf และ %[^\n] เพื่ออนุญาตให้รับข้อความที่มีช่องว่าง.

// หลังจากผู้ใช้ป้อนชื่อ, โปรแกรมจะเรียกใช้ฟังก์ชัน cutify โดยส่งตำแหน่งของอาร์เรย์ name เข้าไปในฟังก์ชัน.

// ในฟังก์ชัน cutify, โปรแกรมจะเข้าสู่ลูป while โดยที่ *p คือตัวอักษรที่ตำแหน่งปัจจุบันของ p.

// ในแต่ละรอบของลูป while, โปรแกรมจะตรวจสอบว่า *p เป็น 'a', 'e', 'o', หรือ 'u' หรือไม่ ถ้าใช่ จะแทนที่ *p ด้วย 'i' โดยใช้ *p = 'i' และหาก *p เป็น 'A', 'E', 'O', หรือ 'U' โปรแกรมจะแทนที่ *p ด้วย 'I' โดยใช้ *p = 'I'.

// หลังจากการตรวจสอบและแทนที่ทุกตัวอักษรที่ต้องการ, โปรแกรมจะเลื่อนตำแหน่งของ p ไปยังตัวอักษรถัดไป.

// เมื่อลูป while สิ้นสุดการทำงาน, แปลงชื่อเสร็จสิ้นและโปรแกรมจะแสดงผลลัพธ์ "Cutify name : " พร้อมกับชื่อที่ผ่านการแปลงแล้วผ่านคำสั่ง printf.